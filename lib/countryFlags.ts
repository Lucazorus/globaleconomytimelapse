/**
 * Returns the flag emoji for a given country name.
 * Falls back to empty string if country not found.
 */
export function flagEmoji(countryName: string): string {
  const code = COUNTRY_FLAGS[countryName];
  if (!code) return "";
  return code.toUpperCase().split("").map(
    (c) => String.fromCodePoint(c.charCodeAt(0) + 127397)
  ).join("");
}
